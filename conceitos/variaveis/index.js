/**
 * Sobre variaveis
 * 
 * Elas pode ser declaradas com o "var" e se pode atribuir valor com o sinal de "=".
 */

var olamundo = "ola mundo";

console.log(olamundo);

// Nas versões mais novas existe o Let que declara variaveis por escopo de atuação.
let tudobem = "tudo bom";

console.log(tudobem);